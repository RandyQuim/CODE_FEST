
public class Step {
	private String step;
	private boolean mistake;

	public String getStep() {
		return step;
	}
	public void setStep(String step) {
		this.step = step;
	}
	public boolean isMistake() {
		return mistake;
	}
	public void setMistake(boolean mistake) {
		this.mistake = mistake;
	}


}
